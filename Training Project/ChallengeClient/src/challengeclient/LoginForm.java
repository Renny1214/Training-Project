    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean flag = validateInput();
        if (flag == true) {
            components.User obj = new components.User();
            obj.UserId = jTextField1.getText();
            obj.Password = jPasswordField1.getText();
            obj.CommandName = "Login";

            try {
                Socket socket = new Socket(ChallengeClient.IPAddress, ChallengeClient.PortNo);
                ObjectOutputStream oos = new ObjectOutputStream(socket.getOutputStream());
                oos.writeObject(obj);

                ObjectInputStream ois = new ObjectInputStream(socket.getInputStream());
                User feedback = (User) ois.readObject();

                if (feedback.UserNo == 0) {

                    JOptionPane.showMessageDialog(this, "user doesnot exist", ChallengeClient.ApplicationName, JOptionPane.WARNING_MESSAGE);
                } else {
                    ChallengeClient.LoggedUser = feedback;
                    Welcome wel = new Welcome(null, true);
                    wel.setVisible(true);
                    this.dispose();
                }

            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

